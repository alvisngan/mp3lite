/*
 * The Huffman code (hcod) are represented as unsigned integer arrays with
 * table 1 to 15 represented as uint8_t and table 16 to 31 as uint16_t 
 *
 * For big _values, the x and y values can be found with idx and max_xy 
 * with the following equations:
 *    x = (idx - y) / (max_xy + 1)
 *    y = idx - x * (max_xy + 1)
 *
 * For count1 quadruples , the v, w, x and y values can be found 
 * with idx with the following equations:
 *    v = (idx & 0x8)? 1 : 0
 *    u = (idx & 0x4)? 1 : 0
 *    x = (idx & 0x2)? 1 : 0
 *    y = (idx & 0x1)? 1 : 0
 * (where & is bitwise the AND operation)
 *
 * hcod arrays are casted to void* because table 1 to 15 are uint8_t*
 * and 16 to 31 are uint16_t*
 *
 * Reference: ISO/IEC 11172-3:1993 Table B.7.
 */
typedef struct {
    uint8_t num;
    uint8_t xy_max;
    uint8_t hlen_arrlen;
    uint8_t *hlen;
    uint8_t *hlen_cnt;
    void *hcod;
    uint8_t *idx;
} huffman_table_t;

static void s_init_huffman_table(huffman_table_t *htb,
                                 uint8_t htb_num,
                                 uint8_t htb_xy_max,
                                 uint8_t htb_hlen_arrlen,
                                 uint8_t *htb_hlen,
                                 uint8_t *htb_hlen_cnt,
                                 void *htb_hcod,
                                 uint8_t *htb_idx)
{
    assert(htb);
    assert(htb_hlen_arrlen > 0);
    assert(htb_hlen);
    assert(htb_hlen_cnt);
    assert(htb_hcod);
    assert(htb_idx);

    htb->num = htb_num;
    htb->xy_max = htb_xy_max;
    htb->hlen_arrlen = htb_hlen_arrlen;
    htb->hlen = htb_hlen;
    htb->hlen_cnt = htb_hlen_cnt;
    htb->hcod = htb_hcod;
    htb->idx = htb_idx;
}


